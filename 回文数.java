class Solution {
    public boolean isPalindrome(int x) {
        int num=x;
        int y=0;
        for(int len=0;num!=0;len++){
            y*=10;
            y+=num%10;
            num=num/10;
        }
        if(x==y)
            if(x>=0){
                return true;
            }else{
                return false;
            }
        else
        return false;
    }
}
